import turtle
t = turtle.Pen()

def правильний_багатокутник(сторона,вершини):
    кут = ((вершини-2)*180)/вершини
    for x in range(0,вершини):
        t.forward(сторона)
        t.left(180 - кут)

def зірка(промінь,вершини):
    кут = 180 + (360/вершини)
    print(кут)
    for x in range(0,вершини):
        t.forward(промінь)
        t.left(кут+20)
        t.forward(промінь)
        t.left(160)

